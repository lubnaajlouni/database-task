create table customers (
id int,
fname varchar(20),
lname varchar(20),
age int,
);
SELECT * FROM Customers;


create table orders (
id int,
price float,
product varchar(20),
total float,
);
SELECT * FROM orders;


create table Products(
id int,
product varchar(20),
productPrice float,
);
SELECT * FROM Products;


create table Employees(
id int,
number int,
fname varchar(20),
);
SELECT * FROM Employees;

